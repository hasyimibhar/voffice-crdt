const bgTextures = {
  0: 'ground.png',
  1: 'grass.png',
};

const fgTextures = {
  1: 'block.png',
  2: 'cactus.png',
};

PIXI.Loader.shared.add('background.json').load(setupBg);

const bgLayer = layers[0];
const fgLayer = layers[1];
const playersLayer = layers[2];

const bgContainer = new PIXI.Container();
const fgContainer = new PIXI.Container();

function setupBg() {
  const sheet = PIXI.Loader.shared.resources['background.json'].spritesheet;

  for (let y = 0; y < 20; y++) {
    for (let x = 0; x < 20; x++) {
      const id = bgLayer[y][x];
      const tile = new PIXI.Sprite(sheet.textures[bgTextures[id]]);
      tile.position.set(x * 32, y * 32);
      bgContainer.addChild(tile);
    }
  }

  app.stage.addChild(bgContainer);

  PIXI.Loader.shared.add('foreground.json').load(setupFg);
}

let player = null;

function setupFg() {
  const sheet = PIXI.Loader.shared.resources['foreground.json'].spritesheet;

  for (let y = 0; y < 20; y++) {
    for (let x = 0; x < 20; x++) {
      const id = fgLayer[y][x];
      if (id > 0) {
        const texture = sheet.textures[fgTextures[id]];
        const tile = new PIXI.Sprite(texture);

        if (texture.height > 32) {
          tile.anchor.set(0, (texture.height-32)/texture.height);
        }

        tile.position.set(x * 32, y * 32);
        fgContainer.addChild(tile);
      }
    }
  }

  player = new PIXI.Sprite(sheet.textures['player.png']);
  app.stage.addChild(player);

  const playerX = player.position.x;
  const playerY = player.position.y;
  const tileX = playerX / 32;
  const tileY = playerY / 32;
  playersLayer[tileY][tileX] = 1;

  app.stage.addChild(fgContainer);
}

document.addEventListener('keydown', (event) => {
  let playerX = player.position.x;
  let playerY = player.position.y;

  const oldTileX = playerX / 32;
  const oldTileY = playerY / 32;

  switch (event.key) {
    case "Down":
    case "ArrowDown":
      playerY += 32;
      break;

    case "Up":
    case "ArrowUp":
      playerY -= 32;
      break;

    case "Left":
    case "ArrowLeft":
      playerX -= 32;
      break;

    case "Right":
    case "ArrowRight":
      playerX += 32;
      break;
  }

  const tileX = playerX / 32;
  const tileY = playerY / 32;

  if (fgLayer[tileY][tileX] == 0 && playersLayer[tileY][tileX] == 0) {
    player.position.set(playerX, playerY);
    playersLayer[oldTileY][oldTileX] = 0;
    playersLayer[tileY][tileX] = 1;
  
    fgContainer.children.sort(function(a, b) {
      if (a.position.y > b.position.y) return 1;
      if (a.position.y < b.position.y) return -1;
      if (a.position.x > b.position.x) return 1;
      if (a.position.x < b.position.x) return -1;
      return 0;
    });
  }
});

